float aireAnneau(float a, float b) 		 				  		 				   
{ 		 				  		 				   
    if (a > b){ 		 				  		 				   
        return ((float)PI * a * a) - ((float)PI * b * b); 		 				  		 				   
    }else{ 		 				  		 				   
        return ((float)PI * b * b) - ((float)PI * a * a); 		 				  		 				   
    } 		 				  		 				   
} 		 				  		 				   