class Point { 		 				  		 				   
    float x; 		 				  		 				   
    float y; 		 				  		 				   
 		 				  		 				   
    Point(){ 		 				  		 				   
        this.x = (float)1.0; 		 				  		 				   
        this.y = (float)1.0; 		 				  		 				   
    } 		 				  		 				   
 		 				  		 				   
    Point(float x, float y){ 		 				  		 				   
       this.x = x; 		 				  		 				   
       this.y = y; 		 				  		 				   
    } 		 				  		 				   
} 		 				  		 				   